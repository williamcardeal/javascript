/*Funções são ações. Portanto, seu nome geralmente é um verbo. Deve ser breve, o mais preciso possível e descrever o que a função faz, para que alguém que leia o código tenha uma indicação do que a função faz.

É uma prática comum iniciar uma função com um prefixo verbal que descreva vagamente a ação. Deve haver um acordo dentro da equipe sobre o significado dos prefixos.

Por exemplo, as funções que começam com "show"geralmente mostram algo.

Função que começa com…

"get…"– retornar um valor,
"calc…"– calcular algo,
"create…"– criar algo,
"check…"– verifica algo e retorna um booleano, etc.*/
